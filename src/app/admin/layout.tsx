import { createServerClient } from '@supabase/ssr'
import { cookies } from 'next/headers'
import { redirect } from 'next/navigation'
import Link from 'next/link'
import { StudyULogo } from '@/components/ui/StudyULogo'

const navigation = [
  { name: 'Áttekintés', href: '/admin', icon: DashboardIcon },
  { name: 'Foglalások', href: '/admin/foglalasok', icon: CalendarIcon },
  { name: 'Naptár', href: '/admin/naptar', icon: CalendarViewIcon },
  { name: 'Felhasználók', href: '/admin/felhasznalok', icon: UsersIcon },
  { name: 'Időpontok', href: '/admin/idopontok', icon: ClockIcon },
  { name: 'Extrák', href: '/admin/extrak', icon: SparklesIcon },
  { name: 'Kuponok', href: '/admin/kuponok', icon: CouponIcon },
  { name: 'Nyitvatartás', href: '/admin/nyitvatartas', icon: StoreIcon },
  { name: 'Ünnepnapok', href: '/admin/unnepnapok', icon: StarIcon },
  { name: 'Belső blokkok', href: '/admin/blokkok', icon: BlockIcon },
  { name: 'Beállítások', href: '/admin/beallitasok', icon: SettingsIcon },
]

export default async function AdminLayout({
  children,
}: {
  children: React.ReactNode
}) {
  const cookieStore = await cookies()
  const supabase = createServerClient(
    process.env.NEXT_PUBLIC_SUPABASE_URL!,
    process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!,
    {
      cookies: {
        getAll() {
          return cookieStore.getAll()
        },
        setAll(cookiesToSet) {
          try {
            cookiesToSet.forEach(({ name, value, options }) =>
              cookieStore.set(name, value, options)
            )
          } catch {
            // Server Component
          }
        },
      },
    }
  )

  const { data: { user } } = await supabase.auth.getUser()

  if (!user) {
    redirect('/auth/login')
  }

  const { data: profile } = await supabase
    .from('profiles')
    .select('role')
    .eq('id', user.id)
    .single()

  if (profile?.role !== 'admin') {
    redirect('/dashboard')
  }

  return (
    <div className="min-h-screen bg-white">
      {/* Sidebar */}
      <aside className="fixed inset-y-0 left-0 w-64 bg-black border-r-[3px] border-black">
        {/* Logo */}
        <div className="h-20 flex items-center justify-center border-b-[3px] border-gray-800">
          <Link href="/" className="flex items-center gap-3">
            <StudyULogo className="h-7" color="#FFFFFF" />
            <span className="text-[10px] uppercase tracking-widest text-[var(--bauhaus-yellow)] font-bugrino">
              Admin
            </span>
          </Link>
        </div>

        {/* Navigation */}
        <nav className="mt-6 px-4">
          {navigation.map((item) => (
            <Link
              key={item.name}
              href={item.href}
              className="group flex items-center gap-3 px-4 py-3 mb-1 text-gray-400 hover:text-white hover:bg-gray-900 transition-colors relative"
            >
              <item.icon className="h-5 w-5" />
              <span className="font-bugrino text-sm uppercase tracking-wider">
                {item.name}
              </span>
              {/* Hover indicator */}
              <span
                className="absolute left-0 top-1/2 -translate-y-1/2 w-1 h-0 group-hover:h-8 transition-all"
                style={{ backgroundColor: 'var(--bauhaus-yellow)' }}
              />
            </Link>
          ))}
        </nav>

        {/* Geometric decoration */}
        <div className="absolute bottom-24 left-4 right-4 flex items-center justify-center gap-3 opacity-20">
          <div className="w-8 h-8 rounded-full border-2 border-white" />
          <div
            className="w-6 h-6"
            style={{
              backgroundColor: 'var(--bauhaus-red)',
              transform: 'rotate(45deg)',
            }}
          />
          <div
            className="w-0 h-0"
            style={{
              borderLeft: '12px solid transparent',
              borderRight: '12px solid transparent',
              borderBottom: '21px solid var(--bauhaus-blue)',
            }}
          />
        </div>

        {/* Back to dashboard */}
        <div className="absolute bottom-0 left-0 right-0 p-4 border-t-[2px] border-gray-800">
          <Link
            href="/dashboard"
            className="flex items-center gap-3 px-4 py-3 text-gray-500 hover:text-white transition-colors group"
          >
            <svg className="h-5 w-5" fill="none" viewBox="0 0 24 24" strokeWidth={2} stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" d="M9 15L3 9m0 0l6-6M3 9h12a6 6 0 010 12h-3" />
            </svg>
            <span className="font-bugrino text-sm uppercase tracking-wider">
              Vissza
            </span>
          </Link>
        </div>
      </aside>

      {/* Main content */}
      <main className="ml-64">
        {/* Top bar */}
        <div className="h-20 border-b-[3px] border-black flex items-center px-8">
          <div className="flex items-center gap-4">
            <div
              className="w-3 h-3 rounded-full"
              style={{ backgroundColor: 'var(--bauhaus-red)' }}
            />
            <div
              className="w-3 h-3 rounded-full"
              style={{ backgroundColor: 'var(--bauhaus-yellow)' }}
            />
            <div
              className="w-3 h-3 rounded-full"
              style={{ backgroundColor: 'var(--bauhaus-blue)' }}
            />
          </div>
        </div>

        {/* Page content */}
        <div className="p-8">
          {children}
        </div>
      </main>
    </div>
  )
}

